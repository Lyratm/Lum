export type Language = 'fr' | 'en' | 'es' | 'zh';

export const getBrowserLanguage = (): Language => {
  if (typeof window === 'undefined') return 'fr';
  const lang = navigator.language || (navigator.languages && navigator.languages[0]) || 'fr';
  const cleanLang = lang.toLowerCase();
  if (cleanLang.startsWith('zh')) return 'zh';
  if (cleanLang.startsWith('es')) return 'es';
  if (cleanLang.startsWith('en')) return 'en';
  return 'fr'; // default to French since the original app is French
};

